    private void ButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSaveActionPerformed
        String namaLapangan = InputNamaLapangan.getText();
        double harga = Double.parseDouble(InputHargaperJam.getText());
        double luas = Double.parseDouble(InputLuas.getText());

        String jenisLapangan = ComboBoxJenisLapangan.getSelectedItem().toString();
        String namaOlahraga = ComboBoxJenisOlahraga.getSelectedItem().toString();
        Olahraga olahraga = new DAOOlahraga().getOlahragaByName(namaOlahraga);

        LokasiLapangan lokasi = lapangan.getLokasi();
        lokasi.setJalan(InputJalan.getText());
        lokasi.setRt_rw(InputRTRW.getText());
        lokasi.setKelurahan(InputKelurahan.getText());
        lokasi.setKecamatan(InputKecamatan.getText());
        lokasi.setKota(InputKabupatenKota.getText());
        lokasi.setProvinsi(InputProvinsi.getText());
        new DAOLokasiLapangan().updateLokasiLapangan(lokasi);

        Fasilitas fasilitas = lapangan.getFasilitas();
        fasilitas.setToilrt(InputToilet.getText());
        fasilitas.setJenis(jenisLapangan);
        fasilitas.setArea_parkir(InputAreaParkir.getText());
        fasilitas.setWifi(InputWifi.getText());
        fasilitas.setPerlengkapan(InputPerlengkapan.getText());
        fasilitas.setAkses_listrik(InputAksesListrik.getText());
        fasilitas.setPencahayaan(InputPencahayaan.getText());
        fasilitas.setP3k(InputP3K.getText());
        fasilitas.setLainnya(InputLainnya.getText());
        new DAOFasilitasLapangan().updateFasilitas(fasilitas);

        lapangan.setNama_lapangan(namaLapangan);
        lapangan.setHarga(harga);
        lapangan.setLuas(luas);
        lapangan.setOlahraga(olahraga);
        lapangan.setFasilitas(fasilitas);
        lapangan.setLokasi(lokasi);
        new DAOLapangan().updateLapangan(lapangan);

        this.dispose();
    }//GEN-LAST:event_ButtonSaveActionPerformed

