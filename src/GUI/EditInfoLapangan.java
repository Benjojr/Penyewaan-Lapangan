    private void isiComboBoxJenisOlahraga() {
        DAOOlahraga daoOlahraga = new DAOOlahraga();
        List<Olahraga> listOlahraga = daoOlahraga.getAllOlahraga();

        ComboBoxJenisOlahraga.removeAllItems();
        ComboBoxJenisOlahraga.addItem("-- Pilih Jenis Olahraga --");
        for (Olahraga o : listOlahraga) {
            ComboBoxJenisOlahraga.addItem(o.getNama_olahraga());
        }
    }
    
    
    private void isiFormData() {
        InputNamaLapangan.setText(lapangan.getNama_lapangan());
        InputHargaperJam.setText(String.valueOf(lapangan.getHarga()));
        InputLuas.setText(String.valueOf(lapangan.getLuas()));

        ComboBoxJenisLapangan.setSelectedItem(lapangan.getFasilitas().getJenis());
        ComboBoxJenisOlahraga.setSelectedItem(lapangan.getOlahraga().getNama_olahraga());

        LokasiLapangan lokasi = lapangan.getLokasi();
        InputJalan.setText(lokasi.getJalan());
        InputRTRW.setText(lokasi.getRt_rw());
        InputKelurahan.setText(lokasi.getKelurahan());
        InputKecamatan.setText(lokasi.getKecamatan());
        InputKabupatenKota.setText(lokasi.getKota());
        InputProvinsi.setText(lokasi.getProvinsi());

        Fasilitas fasilitas = lapangan.getFasilitas();
        InputToilet.setText(fasilitas.getToilrt());
        InputAreaParkir.setText(fasilitas.getArea_parkir());
        InputWifi.setText(fasilitas.getWifi());
        InputPerlengkapan.setText(fasilitas.getPerlengkapan());
        InputAksesListrik.setText(fasilitas.getAkses_listrik());
        InputPencahayaan.setText(fasilitas.getPencahayaan());
        InputP3K.setText(fasilitas.getP3k());
        InputLainnya.setText(fasilitas.getLainnya());
    }
    
